package com.rent.vaca.user;

public class AccoFacilVO {
	private int facilNo;
	private String facilName;
	private String iconName;
	
	public int getFacilNo() {
		return facilNo;
	}
	public void setFacilNo(int facilNo) {
		this.facilNo = facilNo;
	}
	public String getFacilName() {
		return facilName;
	}
	public void setFacilName(String facilName) {
		this.facilName = facilName;
	}
	public String getIconName() {
		return iconName;
	}
	public void setIconName(String iconName) {
		this.iconName = iconName;
	}
	
}
